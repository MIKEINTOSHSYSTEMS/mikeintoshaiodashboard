    // 1. Get logged-in user's ID from session
    $userId = $_SESSION["UserID"]; // Replace with your actual session variable

    // 2. Fetch user data
    $userData = DB::Lookup("SELECT id, username FROM users WHERE id = ?", [$userId]);
    if (!$userData) {
        $result["error"] = "User not found.";
        return false;
    }

    // 3. Generate a RANDOM API key (plaintext + hash)
    $api_key_plain = bin2hex(random_bytes(32)); // 64-character random string
    $api_key_hashed = password_hash($api_key_plain, PASSWORD_DEFAULT); // Hash it

    // 4. Update the `api_key` column in the database
    $success = DB::Exec(
        "UPDATE users SET api_key = ? WHERE id = ?",
        [$api_key_hashed, $userId]
    );

    if (!$success) {
        $result["error"] = "Failed to save API key.";
        return false;
    }

    // 5. Return the plaintext key to display ONCE to the user
    $result["api_key"] = $api_key_plain;

    // 6. Optional: Store in session (use consistent naming)
    $_SESSION["api_key"] = $api_key_plain; // Or store the hash if needed

    return true;
;
	RunnerContext::pop();
	echo my_json_encode($result);
	$button->deleteTempFiles();
